exports.add = function (firstNum, secondNum) {
    return firstNum + secondNum;
};

exports.sub = function (firstNum, secondNum) {
    return firstNum - secondNum;
};

exports.mul = function (firstNum, secondNum) {
    return firstNum * secondNum;
};

exports.div = function (firstNum, secondNum) {
    return firstNum / secondNum;
};